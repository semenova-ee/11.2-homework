def upper_string(somestring) -> str:
    '''Функция принимает строку
    возвращает строку заглавными буквами'''
    return somestring.upper()

def title_string(somestring) -> str:
    '''Функция принимает строку
    возвращает строку, в которой каждое слово с заглавной буквы'''
    return somestring.title()